SELECT
    `empid`,
    case
        WHEN year(`startdate`) = year(current_date())
        and month(`startdate`) = month(current_date()) THEN CASE
            WHEN DAY(`startdate`) = DAY(LAST_DAY(current_date())) THEN 0
            else 30 - DAY(`startdate`)
        end
        else 30
    END AS dias,
    CONCAT(
        CAST(year(current_date()) as NCHAR(4)),
        CASE
            WHEN month(current_date()) < 10 then CONCAT('0', cast(month(current_date()) as NCHAR(2)))
            else month(current_date())
        end
    ) gest
FROM
    RRHH.employee;


